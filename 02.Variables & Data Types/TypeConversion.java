import java.util.Scanner;

public class TypeConversion {
    public static void main(String[] args) {
        // int a = 25;
        // long b = a;

        // int a = 15;
        // float b = a;

        float a = 3.2f;
        double b = a;
        
        System.out.println(b);

        Scanner sc = new Scanner(System.in);
        float number = sc.nextInt();
        System.out.println(number);
    }
}
