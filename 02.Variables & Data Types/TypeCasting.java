public class TypeCasting {
    public static void main(String[] args) {
        // float a = 25.999f;
        // int b = (int) a;

        double a = 3.141521035;
        float b = (float) a;
        System.out.println(b);

        char ch = 'a';
        char ch1 = 'b';
        int number = ch;
        int number1 = ch1;
        System.out.println(number);
        System.out.println(number1);
    }
}
