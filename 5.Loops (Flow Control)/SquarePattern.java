public class SquarePattern {
    public static void main(String[] args) {
        
        int j = 1;
        while(j<=4) {
            System.out.println("* * * *");
            j++;
        }
        System.out.println("");
        for(int i=1; i<=4; i++) {
            System.out.println("* * * *");
        }
    }
}
